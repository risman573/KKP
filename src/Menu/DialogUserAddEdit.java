    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String sql = "INSERT INTO users(name, username, email, nohp, alamat, password) VALUES (?,?,?,?,?,?)";
        if (id > 0){
            sql = "UPDATE users SET name = ?, username = ?, email = ?, nohp = ?, alamat = ?, password = ? WHERE id = ?";
        }
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, txtNama.getText());
                stat.setString(2, txtUsername.getText());
                stat.setString(3, txtEmail.getText());
                stat.setString(4, txtNoHP.getText());
                stat.setString(5, txtAlamat.getText());
                stat.setString(6, txtPassword.getText());
                if (id > 0){
                    stat.setLong(7, id);    
                }
                stat.executeUpdate();
                if (id > 0) {
                    JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
                } else {
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                }

                dispose();
            }catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
            }
    }//GEN-LAST:event_btnSaveActionPerformed

